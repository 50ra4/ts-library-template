/**
 * add
 * @param n1 number1
 * @param n2 number2
 * @returns number1 + number2
 */
export const add = (n1: number, n2: number): number => n1 + n2;
/**
 * inc
 * @param n number
 * @returns number + 1
 */
export const inc = (n: number) => n + 1;
/**
 * dec
 * @param n number
 * @returns number - 1
 */
export const dec = (n: number) => n - 1;
